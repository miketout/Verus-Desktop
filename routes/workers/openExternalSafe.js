function generateOpenExternalSafe(shell, url) {
  return async (inputUrl) => {
    const URL = url.parse(inputUrl);

    if (URL.protocol === 'https:' && allowedOrigins.includes(URL.host)) {
      shell.openExternal(inputUrl)
      return 0
    } else {
      return -1
    }
  }
}

module.exports = generateOpenExternalSafe
